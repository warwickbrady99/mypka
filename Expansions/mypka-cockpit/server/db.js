// db.js — opens mypka.db READ-ONLY. The cockpit never writes to the database.
// Markdown is canonical; mypka.db is a derived mirror (regenerable at any time
// via Expansions/mypka-cockpit/scripts/regen-mypka-db.py).
import Database from 'better-sqlite3';
import path from 'node:path';
import fs from 'node:fs';
// REPO_ROOT is resolved by the shared resolver (repoRoot.js): MYPKA_ROOT env →
// upward search for the AGENTS.md + PKM/ scaffold fingerprint → 3-up fallback.
// db.js re-exports it so existing importers (server.js, gpxRoute.js, markdown.js)
// keep importing REPO_ROOT from here unchanged.
import { REPO_ROOT } from './repoRoot.js';

export { REPO_ROOT };
const DB_PATH = path.resolve(REPO_ROOT, 'mypka.db');

if (!fs.existsSync(DB_PATH)) {
  throw new Error(
    `mypka.db not found at ${DB_PATH}.\n\n` +
    `  The cockpit reads a SQLite mirror of your markdown myPKA. Generate it with:\n` +
    `    python3 "Expansions/mypka-cockpit/scripts/regen-mypka-db.py"\n` +
    `  (run from your myPKA root; needs Python 3 + PyYAML: pip3 install --user pyyaml)\n\n` +
    `  This is non-destructive: your markdown stays the source of truth, the .db\n` +
    `  is a derived layer. See Expansions/mypka-cockpit/ADAPT-EXPANSION.md.`
  );
}

// readonly + fileMustExist; query_only pragma is a second belt.
const db = new Database(DB_PATH, { readonly: true, fileMustExist: true });
db.pragma('query_only = true');

// ---- Schema preflight ---------------------------------------------------------
// The cockpit prepares its SQL at module load, so a missing table would otherwise
// surface as a cryptic prepare() error. Check the contract up front and fail with
// an actionable message instead. The full contract lives in docs/db-contract.md.
const REQUIRED_TABLES = [
  'people', 'organizations', 'topics', 'projects', 'goals',
  'key_elements', 'habits', 'documents', 'deliverables', 'journal',
  'journal_media', 'links', 'agents',
];
const present = new Set(
  db.prepare(`SELECT name FROM sqlite_master WHERE type IN ('table','view')`)
    .all()
    .map((r) => r.name)
);
const missing = REQUIRED_TABLES.filter((t) => !present.has(t));
if (missing.length) {
  throw new Error(
    `mypka.db at ${DB_PATH} is missing table(s) the cockpit needs: ${missing.join(', ')}.\n\n` +
    `  Your database was likely generated by a different script. The cockpit ships\n` +
    `  its own generator that produces (or refreshes) exactly these tables without\n` +
    `  touching any other tables in the file:\n` +
    `    python3 "Expansions/mypka-cockpit/scripts/regen-mypka-db.py"\n\n` +
    `  Schema details: Expansions/mypka-cockpit/docs/db-contract.md`
  );
}

export default db;
export { DB_PATH };
